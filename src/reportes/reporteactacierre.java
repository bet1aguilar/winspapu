 public final void generarpdf() throws DocumentException{
        try {
              FileInputStream input=null;
            try {
                input = new FileInputStream(new File("actainicio.jrxml"));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(reporteactacierre.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            JasperDesign design = null; 
            try {
                design = JRXmlLoader.load(input);
            } catch (JRException ex) {
                Logger.getLogger(reporteactacierre.class.getName()).log(Level.SEVERE, null, ex);
            }
            JasperReport report = JasperCompileManager.compileReport(design);
            Map parameters = new HashMap();         
       
            parameters.put("fecha", format.format(jDateChooser1.getDate()));
            parameters.put("obra", jTextField1.getText().toString());
            parameters.put("ubicacion", jTextField2.getText().toString());
            parameters.put("contratista", jTextField3.getText().toString());
            parameters.put("representante", jTextField4.getText().toString());
            parameters.put("cedrepresentante", jTextField5.getText().toString());
            parameters.put("residente", jTextField7.getText().toString());
            parameters.put("cedresidente", jTextField8.getText().toString());
            parameters.put("civresidente", jTextField9.getText().toString());
            parameters.put("inspector", jTextField10.getText().toString());
            parameters.put("cedinspector", jTextField11.getText().toString());
            parameters.put("civinspector", jTextField12.getText().toString());
            parameters.put("titulo", jTextField6.getText().toString());
            parameters.put("texto", jTextArea1.getText().toString());
            parameters.put("texto2", jTextArea2.getText().toString());
            parameters.put("mpres", mpres);
            JasperPrint print = JasperFillManager.fillReport(report, parameters, conex);
           
            
            JasperViewer.viewReport(print, false);
            
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, "No se logró abrir el archivo "+ex.getMessage());
            Logger.getLogger(reporteactacierre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
