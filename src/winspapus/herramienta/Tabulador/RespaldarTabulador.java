 public void verificaHilos()
    {
        hilos--;
        if(hilos==0)
        {
            sqlInsert.append(mtabus).append(ctabs).append(deptabs).
                    append(dmoptabs).append(dmtabs).append(mbdats).append(mconts).
                    append(metabs).append(mmotabs).append(mmtabs).append(mprops).
                    append(mptabs);
            File file = new File(ruta);
            try {
                PrintWriter print = new PrintWriter(file);
                print.write(sqlInsert.toString());
                print.close();
                JOptionPane.showMessageDialog(rootPane, "Se ha realizado el respaldo de tabulador "+mtabu+" "
                        + "en la ruta "+ruta+". Debe tomar este archivo para respaldar "
                        + "el tabulador en su otro dispositivo." );
                
             doClose(RET_OK);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(RespaldarTabulador.class.getName()).log(Level.SEVERE, null, ex);
            }
            doClose(RET_OK);
        }
    }
