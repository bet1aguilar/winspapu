    public final void llena(){
        String select = "SELECT o.nombre, o.ubicac, c.nombre, c.repleg, c.cedrep, c.ingins, c.cedins, "
                + "c.civins, c.ingres, c.cedres, c.civres FROM mpres as o, mconts as c WHERE "
                + "o.id='"+mpres+"' AND (o.codcon = c.id OR o.codcon IS NULL)";
        try {
            Statement st = (Statement) conex.createStatement();
            ResultSet rst = st.executeQuery(select);
            while(rst.next()){
                jTextField1.setText(rst.getString(1));
                jTextField2.setText(rst.getString(2));
                jTextField3.setText(rst.getString(3));
                jTextField4.setText(rst.getString(4));
                jTextField5.setText(rst.getString(5));
                jTextField7.setText(rst.getString("c.ingres"));
                jTextField8.setText(rst.getString("c.cedres"));
                jTextField9.setText(rst.getString("c.civres"));
                jTextField10.setText(rst.getString("c.ingins"));
                jTextField11.setText(rst.getString("c.cedins"));
                jTextField12.setText(rst.getString("c.civins"));
                jTextArea2.setText(preTexto2+" "+jTextField2.getText().toString()+", con fecha "+format.format(jDateChooser1.getDate()));
            }
        } catch (SQLException ex) {
            Logger.getLogger(reporteactacierre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
