    private void iniciaRespaldo()
    {
        //REPLACE INTO de mtabus
        int cont=0;
        mtabus = mtabu+";\n";
        String sql = "SELECT id, descri, vigencia, IFNULL(padyga,0) as padyga, IFNULL(pcosfin, 0) as pcosfin, "
                + "IFNULL(pimpue, 0) as pimpue, IFNULL(pprest, 0) as pprest, IFNULL(putild,0) as putild, IFNULL(status,'') as status, "
                + "IFNULL(seleccionado,0) as seleccionado, IFNULL(mcont,'') as mcont, IFNULL(mprop, '') as mprop, IFNULL(oficial,0) as oficial,"
                + "IFNULL(bloqueado,0) as bloqueado FROM mtabus WHERE id='"+mtabu+"'";
        try {
            Statement st = conex.createStatement();
            ResultSet rst = st.executeQuery(sql);
            while(rst.next())
            {
                String vigencia = rst.getString("vigencia");
                if(cont>0)
                    mtabus = mtabus+",\n";
                if(cont==0)
                    mtabus=mtabus+"REPLACE INTO mtabus (id, descri, vigencia, padyga, pcosfin, pimpue, pprest, putild, status"
                            + ",seleccionado, mcont, mprop, oficial, bloqueado) VALUES \n";
                mtabus = mtabus+"('"+rst.getString("id")+"','"+rst.getString("descri") +"',"
                        + "IF("+vigencia+" IS NULL, NULL, '"+vigencia+"'), "+rst.getString("padyga") +","
                        + ""+rst.getString("pcosfin")+", "+rst.getString("pimpue")+","
                        + ""+rst.getString("pprest")+", "+rst.getString("putild")+","
                        + "'"+rst.getString("status")+"', "+rst.getString("seleccionado") +","
                        + "'"+rst.getString("mcont")+"', '"+rst.getString("mprop")+"',"
                        + ""+rst.getString("oficial")+", "+rst.getString("bloqueado")+")";
                
                cont++;
            }
            if(cont>0)
                mtabus = mtabus+";\n";
            cont=0;
        } catch (SQLException ex) {
            Logger.getLogger(RespaldarTabulador.class.getName()).log(Level.SEVERE, null, ex);
        }
        ThreadCtab tctab = new ThreadCtab(conex, mtabu, this);
        tctab.start();
        ThreadDeptabs tdeptabs = new ThreadDeptabs(conex, mtabu, this);
        tdeptabs.start();
        ThreadDmoptabs tdmoptabs = new ThreadDmoptabs(conex, mtabu, this);
        tdmoptabs.start();
        ThreadDmtabs tdmtabs = new ThreadDmtabs(conex, mtabu, this);
        tdmtabs.start();
        ThreadMbdats tmbdats = new ThreadMbdats(conex, mtabu, this);
        tmbdats.start();
        ThreadMconts tmconts = new ThreadMconts(conex, mtabu, this);
        tmconts.start();
        ThreadMetabs tmetabs = new ThreadMetabs(conex, mtabu, this);
        tmetabs.start();
        ThreadMmotabs tmmotabs = new ThreadMmotabs(conex, mtabu, this);
        tmmotabs.start();
        ThreadMmtabs tmmtabs = new ThreadMmtabs(conex, mtabu, this);
        tmmtabs.start();
        ThreadMprops tmprops = new ThreadMprops(conex, mtabu, this);
        tmprops.start();
        ThreadMptabs tmtabs = new ThreadMptabs(conex, mtabu, this);
        tmtabs.start();        
            /*ThreadMprops tmprops = new ThreadMprops(conex, mpres, this);
            tmprops.start();*/
        
        
    }
